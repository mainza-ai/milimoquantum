"""Milimo Quantum — Notebook Export.

Generate Jupyter .ipynb notebooks from conversation code artifacts
and quantum circuit execution data.
"""
from __future__ import annotations

import json
import logging

logger = logging.getLogger(__name__)


def generate_notebook(
    title: str = "Milimo Quantum Experiment",
    code_cells: list[str] | None = None,
    markdown_cells: list[str] | None = None,
    conversation_messages: list[dict] | None = None,
) -> dict:
    """Generate a Jupyter notebook (.ipynb) JSON structure.

    Args:
        title: Notebook title (used as heading)
        code_cells: List of Python code strings to include as code cells
        markdown_cells: List of markdown strings to include as text cells
        conversation_messages: Optionally extract code from conversation history

    Returns:
        Dict representing the .ipynb JSON structure
    """
    cells = []

    # Title cell
    cells.append(_md_cell(f"# {title}\n\n*Generated by Milimo Quantum*"))

    # If conversation provided, extract code blocks
    if conversation_messages:
        for msg in conversation_messages:
            content = msg.get("content", "")
            role = msg.get("role", "")

            if role == "assistant":
                # Extract Python code blocks
                code_blocks = _extract_code_blocks(content)
                if code_blocks:
                    # Add explanation as markdown
                    explanation = _strip_code_blocks(content)
                    if explanation.strip():
                        cells.append(_md_cell(explanation.strip()))

                    for code in code_blocks:
                        cells.append(_code_cell(code))
                else:
                    cells.append(_md_cell(content))
            elif role == "user":
                cells.append(_md_cell(f"**User:** {content}"))

    # Explicit code cells
    if code_cells:
        cells.append(_md_cell("---\n## Code"))
        for code in code_cells:
            cells.append(_code_cell(code))

    # Explicit markdown cells
    if markdown_cells:
        for md in markdown_cells:
            cells.append(_md_cell(md))

    notebook = {
        "nbformat": 4,
        "nbformat_minor": 5,
        "metadata": {
            "kernelspec": {
                "display_name": "Python 3 (ipykernel)",
                "language": "python",
                "name": "python3",
            },
            "language_info": {
                "name": "python",
                "version": "3.12.0",
                "mimetype": "text/x-python",
                "codemirror_mode": {"name": "ipython", "version": 3},
                "pygments_lexer": "ipython3",
                "file_extension": ".py",
                "nbconvert_exporter": "python",
            },
            "milimo_quantum": {
                "generator": "Milimo Quantum",
                "version": "0.1.0",
            },
        },
        "cells": cells,
    }

    return notebook


def notebook_to_bytes(notebook: dict) -> bytes:
    """Serialize a notebook dict to JSON bytes."""
    return json.dumps(notebook, indent=1).encode("utf-8")


def _md_cell(source: str) -> dict:
    """Create a markdown cell."""
    return {
        "cell_type": "markdown",
        "metadata": {},
        "source": source.split("\n"),
    }


def _code_cell(source: str) -> dict:
    """Create a code cell."""
    return {
        "cell_type": "code",
        "execution_count": None,
        "metadata": {},
        "outputs": [],
        "source": source.split("\n"),
    }


def _extract_code_blocks(text: str) -> list[str]:
    """Extract Python code blocks from markdown text."""
    import re
    pattern = r"```(?:python)?\s*\n(.*?)```"
    return re.findall(pattern, text, re.DOTALL)


def _strip_code_blocks(text: str) -> str:
    """Remove code blocks from markdown text."""
    import re
    return re.sub(r"```(?:python)?\s*\n.*?```", "", text, flags=re.DOTALL)
